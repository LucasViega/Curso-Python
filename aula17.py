primeiro_valor = input("Digite um valor: \n")
segundo_valor = input("Digite outro valor: \n")

if primeiro_valor > segundo_valor:
    print(
        f'Primeiro valor: {primeiro_valor} é maior que do que o segundo valor: {segundo_valor}'
        )
else:
    print(
        f'Segundo valor: {segundo_valor} é maior que do que o primeiro valor: {primeiro_valor}'
        )
